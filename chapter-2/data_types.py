# Integer
x = 10
print("Integer:", x)

# Float
pi = 3.14
print("Float:", pi)

# String
name = "Alice"
print("String:", name)

# Boolean
is_active = True
print("Boolean:", is_active)

# List
numbers = [1, 2, 3, 4]
print("List:", numbers)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Dictionary
person = {"name": "Alice", "age": 25}
print("Dictionary:", person)

# Set
unique_numbers = {1, 2, 3, 4}
print("Set:", unique_numbers)
